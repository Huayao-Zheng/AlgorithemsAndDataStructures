function bubbleSort(array) {
  array = [...array];

  let didItSwap;

  for (let i = 0; i < array.length - 1; i++) {
    for (let j = 0; j < array.length - 1 - i; j++) {
      if (array[j] > array[j + 1]) {
        swap(array, j);
        didItSwap = true;
      }
    }

    if (!didItSwap) return array;
  }

  return array;
}

function swap(array, j) {
  const holder = array[j];
  array[j] = array[j + 1];
  array[j + 1] = holder;
}

